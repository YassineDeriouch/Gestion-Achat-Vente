    public static JFreeChart showBarChart(){
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();  
        Connection cnx = SingletonConnection.getConnection();
        String sql = "SELECT nomProd,quantiteProd,dateEntre FROM `produit` ORDER BY `quantiteProd` ASC ";
        try {
            Statement st = cnx.createStatement();
            ResultSet r = st.executeQuery(sql);
            while(r.next()){
                 
                String nomProd = r.getString("nomProd" ) ;
                int qte = r.getInt( "quantiteProd" );
                Date dateEntre = r.getDate("dateEntre");
                dataset.setValue(qte, nomProd,dateEntre);

            }
       
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        
         JFreeChart chart = ChartFactory.createBarChart("Evolution du stock", "nomProd", "qteProd",dataset, PlotOrientation.VERTICAL, true, true, false);         
         CategoryPlot p = chart.getCategoryPlot();
         ChartPanel BarChartPanel = new ChartPanel(chart);
         
         BarChartPanel.setSize(745, 253);
         chart.getPlot().setBackgroundPaint( new Color(255, 255, 255));
         jBARGRAPH.add(BarChartPanel);
         jBARGRAPH.validate();
               
        return chart;
    } 
      public static JPanel createDemoBar() throws SQLException, ClassNotFoundException {
      JFreeChart chart;  
        chart = showBarChart();
      return new ChartPanel( chart ); 
   }
     
     
