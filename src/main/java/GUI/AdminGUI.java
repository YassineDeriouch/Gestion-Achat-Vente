    public static JFreeChart showPieChart(){
        DefaultPieDataset dataset = new DefaultPieDataset();
        Connection cnx = SingletonConnection.getConnection();
        String sql = "Select nomProd,quantiteProd FROM produit";
        try {
            Statement st = cnx.createStatement();
            ResultSet r = st.executeQuery(sql);
            while(r.next()){
                 dataset.setValue( 
         r.getString( "nomProd" ) ,
         Integer.parseInt( r.getString( "quantiteProd" )));
            }
              
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        
        JFreeChart chart = ChartFactory.createPieChart( "Etat du stock", dataset, true, true,false );          
         ChartPanel pieChartPanel = new ChartPanel(chart);
         pieChartPanel.setSize(320, 247);
         jGRAPH1.add(pieChartPanel);
         jGRAPH1.validate();
               
        return chart;
        
    }
